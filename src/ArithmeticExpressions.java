public class ArithmeticExpressions {
    public static void main(String[] args) {
        int result = 10 + 3;
        System.out.println(result);

        result = 10 - 3;
        System.out.println(result);

        result = 10 * 3;
        System.out.println(result);

        result = 10 / 3;
        System.out.println(result);

        result = 10 % 3;
        System.out.println(result);
    }
}
